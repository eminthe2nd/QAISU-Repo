def update_dynamic_weights(feedback_df, current_weights, alpha=0.05):
    # current_weights = {'w1_cost': 0.2, 'w2_ml': 0.2, 'w3_risk': 0.2, 'w4_anomaly': 0.2, 'w5_supplier': 0.2}

    # 1. Mühendislerin sistemi ezme (override) davranışlarını analiz et
    # "Sistem Kabul Dedi, İnsan Hurda Yaptı" -> Risk ağırlığı artmalı
    risk_arttirici_mudahale = len(feedback_df[(feedback_df['AI_KARAR'] == 'Kabul') &
                                              (feedback_df['INSAN_KARAR'].isin(['Hurda', 'RET']))])

    # "Sistem Hurda Dedi, İnsan Kabul Yaptı" -> Maliyet ağırlığı artmalı (Risk azalmalı)
    maliyet_arttirici_mudahale = len(feedback_df[(feedback_df['AI_KARAR'].isin(['Hurda', 'RET'])) &
                                                 (feedback_df['INSAN_KARAR'] == 'Kabul')])

    toplam_islem = len(feedback_df)
    if toplam_islem == 0:
        return current_weights

    # 2. Değişim (Delta) hesaplama
    delta_risk = (risk_arttirici_mudahale / toplam_islem) - (maliyet_arttirici_mudahale / toplam_islem)
    delta_cost = -delta_risk # Maliyet ve risk ters orantılıdır

    # 3. Ağırlıkları güncelleme (alpha ile yumuşatarak)
    new_weights = current_weights.copy()
    new_weights['w_risk'] += alpha * delta_risk
    new_weights['w_anomaly'] += alpha * delta_risk * 0.5 # Anomali de riske paralel artar
    new_weights['w_cost'] += alpha * delta_cost

    # 4. Toplamın 1.0 ( %100 ) olmasını sağlama (Softmax veya MinMax Normalizasyonu)
    total_w = sum(new_weights.values())
    new_weights = {k: v / total_w for k, v in new_weights.items()}

    return new_weights

def run_optimization_model(df_udf, df_vir, udf_classes, vir_classes, df_isko, dinamik_w=None):

    # 1. Ağırlıkları Nereden Alacağımıza Karar Verelim
    if dinamik_w is not None:
        # Eğer dışarıdan bir ağırlık sözlüğü gönderilmişse (Simülasyon Modu)
        weights = dinamik_w
        calisma_modu = "SİMÜLASYON (DİNAMİK ÖĞRENME)"
    else:
        # Eğer parametre boşsa veritabanına bağlan (Canlı Ortam Modu)
        db_settings = get_current_weights_from_db()
        weights = db_settings
        calisma_modu = db_settings.get('mode', 'static').upper()

    # 2. Ağırlıkları Değişkenlere Ata
    w1 = weights.get('w_cost', 0.20)
    w2 = weights.get('w_ml', 0.20)
    w3 = weights.get('w_risk', 0.20)
    w4 = weights.get('w_anomaly', 0.20)
    w5 = weights.get('w_supplier', 0.20)

    print(f"Sistem şu anda {calisma_modu} modda çalışıyor.")
    print(f"Kullanılan Ağırlıklar -> Maliyet:{w1:.3f}, ML:{w2:.3f}, Risk:{w3:.3f}, Anomali:{w4:.3f}, Tedarikçi:{w5:.3f}")

    df_opt = df_udf.copy()
    df_vir_opt = df_vir.copy()

    # 1. PARAMETRELERİN HAZIRLANMASI (Data Mapping)

    # 1.A. UDF Parametreleri
    C_c = {'RU': 0.6, 'Hurda': 1.0, 'Kabul/OGK': 0.2, 'Islah': 0.5, 'RTV': 0.4}
    C_c = {c: C_c.get(c, 0.5) for c in udf_classes}

    vir_kalem_set = set(df_vir_opt['KALEM'])
    df_opt['VIR_KAYDI_VAR_MI'] = df_opt['KALEM'].apply(lambda x: 1 if x in vir_kalem_set else 0)

    vir_olan_risk = df_opt[df_opt['VIR_KAYDI_VAR_MI'] == 1]['KSYM_SORUMLUSU_KARAR'].isin(['Hurda', 'RET']).mean()
    vir_olmayan_risk = df_opt[df_opt['VIR_KAYDI_VAR_MI'] == 0]['KSYM_SORUMLUSU_KARAR'].isin(['Hurda', 'RET']).mean()

    if pd.isna(vir_olmayan_risk) or vir_olmayan_risk == 0:
        dinamik_m_cross = 1.1
    else:
        dinamik_m_cross = vir_olan_risk / vir_olmayan_risk

    dinamik_m_cross = max(1.0, min(3.0, dinamik_m_cross))
    print(f"\nVeriden türetilen M_cross (Çapraz Kayıt) Çarpanı: {dinamik_m_cross:.2f}x")

    df_opt['M_cross'] = df_opt['KALEM'].apply(lambda x: dinamik_m_cross if x in vir_kalem_set else 1.0)

    # UDF R_i (Kök Neden Risk Skoru)
    udf_riskli = ['Hurda', 'RTV', 'RU', 'Islah']
    udf_kok_riskleri = df_opt.groupby('KSYM_KOK_NEDEN')['KSYM_SORUMLUSU_KARAR'].apply(lambda x: x.isin(udf_riskli).mean()).to_dict()
    df_opt['R_i'] = df_opt['KSYM_KOK_NEDEN'].map(udf_kok_riskleri).fillna(0.3).clip(lower=0.1)

    # 1.B. VIR (Tedarikçi) Parametreleri
    vir_cost_mapping = {'Kabul': 0.1, 'RET': 1.0, 'RU': 0.6, 'Roketsan da değerlendirilecek': 0.3}
    C_c_vir = {c: vir_cost_mapping.get(c, 0.5) for c in vir_classes}

    # VIR R_j (Tedarikçi Kök Neden Risk Skoru)
    vir_riskli = ['RET', 'RU']
    vir_kok_riskleri = df_vir_opt.groupby('VIR_KOK_NEDEN')['VIR_KARAR'].apply(lambda x: x.isin(vir_riskli).mean()).to_dict()
    df_vir_opt['R_j'] = df_vir_opt['VIR_KOK_NEDEN'].map(vir_kok_riskleri).fillna(0.3).clip(lower=0.1)

    # 2. MODELİN KURULMASI VE KARAR DEĞİŞKENLERİ
    prob = pulp.LpProblem("Birlesik_Kalite_Karar_Optimizasyonu", pulp.LpMinimize)

    # x_i,c (UDF Karar Değişkenleri)
    x_udf = pulp.LpVariable.dicts("x_udf", ((i, c) for i in df_opt.index for c in udf_classes), cat='Binary')

    # x_j,c (VIR Karar Değişkenleri)
    x_vir = pulp.LpVariable.dicts("x_vir", ((j, c) for j in df_vir_opt.index for c in vir_classes), cat='Binary')

    # y_j (Etki-Tepki / Gelecek Etki Tahmin Değişkeni)
    y = pulp.LpVariable.dicts("y_j", (j for j in df_vir_opt.index), lowBound=0, upBound=1, cat='Continuous')

    # 3. KISITLAR (CONSTRAINTS)

    # Kısıt 1: Tek Karar Kısıtı
    for i in df_opt.index:
        prob += pulp.lpSum([x_udf[(i, c)] for c in udf_classes]) == 1, f"Tek_Karar_UDF_{i}"

    for j in df_vir_opt.index:
        prob += pulp.lpSum([x_vir[(j, c)] for c in vir_classes]) == 1, f"Tek_Karar_VIR_{j}"

    # Kısıt 2: İSKO (Minimum %90) Kısıtı
    ilgili_is_emirleri = df_opt['IS_EMRI'].unique()
    toplam_baslangic = df_isko[df_isko['IS_EMRI'].isin(ilgili_is_emirleri)]['IS_EMRI_BASLANGIC_MIKTARI'].sum()
    if toplam_baslangic == 0: toplam_baslangic = df_opt['ALIKONULAN_MIKTAR'].sum() * 10

    max_fire = toplam_baslangic * 0.10
    fire_kararlari = [c for c in udf_classes if c in ['Hurda', 'RTV', 'RU']]
    df_opt['ALIKONULAN_MIKTAR'] = pd.to_numeric(df_opt['ALIKONULAN_MIKTAR'], errors='coerce').fillna(0)

    prob += pulp.lpSum([df_opt.loc[i, 'ALIKONULAN_MIKTAR'] * x_udf[(i, c)] for i in df_opt.index for c in fire_kararlari]) <= max_fire, "ISKO_Kisit"

    gecmis_is_yuku = df_opt['MUAYENE_EDEN'].value_counts().to_dict()

    dinamik_kapasite_sozlugu = {}
    for personel, adet in gecmis_is_yuku.items():
        # Minimum 5 iş ver, üstünü geçmiş performansının %20 fazlası olarak ayarla
        dinamik_kapasite_sozlugu[personel] = max(5, int(adet * 1.2) + 5)

    personeller = df_opt['MUAYENE_EDEN'].unique()

    for personel in personeller:
        personel_kayitlari = df_opt[df_opt['MUAYENE_EDEN'] == personel].index
        kisiye_ozel_kapasite = dinamik_kapasite_sozlugu.get(personel, 10) # Bulamazsa default 10

        # Personelin atandığı işlerin toplamı, kendi dinamik kapasitesini aşamaz
        prob += pulp.lpSum([x_udf[(i, c)] for i in personel_kayitlari for c in udf_classes]) <= kisiye_ozel_kapasite, f"Kapasite_{personel}"

    # Kısıt 3: Etki-Tepki Fonksiyonu (VIR -> UDF Transferi)
    kabul_benzeri_vir_kararlari = [c for c in vir_classes if c not in ['RET', 'RU']]
    for j in df_vir_opt.index:
        dinamik_risk_carpani = df_vir_opt.loc[j, 'R_j'] * df_vir_opt.loc[j, 'A_i_Anomali_Skoru']

        prob += y[j] == pulp.lpSum([x_vir[(j, c)] for c in kabul_benzeri_vir_kararlari]) * dinamik_risk_carpani, f"Etki_Tepki_yj_{j}"

    # Eğer ML bir parçaya > %40 Hurda diyorsa, o parçanın Kabul edilmesi yasaklanır.
    if 'P_c_Hurda' in df_opt.columns:
        for i in df_opt.index:
            if df_opt.loc[i, 'P_c_Hurda'] > 0.40:
                prob += x_udf[(i, 'Kabul/OGK')] == 0, f"Kirmizi_Cizgi_Kabul_Engeli_{i}"

    # 4. ÇOK AMAÇLI TOPLAMSAL FONKSİYON (Multi-Objective Additive)

    # Hangi kararların riski/anomaliyi fabrika içine taşıdığını tanımlıyoruz
    risk_tasiyan_kararlar = ['Kabul/OGK', 'Islah']
    vir_risk_tasiyanlar = ['Kabul', 'Roketsan da değerlendirilecek']

    # UDF için ağırlıkların kendi içinde 1'e tamamlanması (w5 hariç)
    w_sum_udf = w1 + w2 + w3 + w4
    w1_udf, w2_udf, w3_udf, w4_udf = w1/w_sum_udf, w2/w_sum_udf, w3/w_sum_udf, w4/w_sum_udf

    # VIR için ağırlıkların kendi içinde 1'e tamamlanması (Tümü dahil)
    w_sum_vir = w1 + w2 + w3 + w4 + w5
    w1_vir, w2_vir, w3_vir, w4_vir, w5_vir = w1/w_sum_vir, w2/w_sum_vir, w3/w_sum_vir, w4/w_sum_vir, w5/w_sum_vir

    # Şimdi PuLP Amaç Fonksiyonunu bu düzeltilmiş ağırlıklarla kuralım:
    Z_maliyet_udf = pulp.lpSum([C_c[c] * df_opt.loc[i, 'M_cross'] * x_udf[(i, c)] for i in df_opt.index for c in udf_classes])
    Z_maliyet_vir = pulp.lpSum([C_c_vir[c] * 1.0 * x_vir[(j, c)] for j in df_vir_opt.index for c in vir_classes])

    Z_ml_udf = pulp.lpSum([(1.0 - df_opt.loc[i, f'P_c_{c}']) * x_udf[(i, c)] for i in df_opt.index for c in udf_classes])
    Z_ml_vir = pulp.lpSum([(1.0 - df_vir_opt.loc[j, f'P_c_{c}']) * x_vir[(j, c)] for j in df_vir_opt.index for c in vir_classes])

    Z_risk_udf = pulp.lpSum([df_opt.loc[i, 'R_i'] * x_udf[(i, c)] for i in df_opt.index for c in udf_classes if c in risk_tasiyan_kararlar])
    Z_risk_vir = pulp.lpSum([df_vir_opt.loc[j, 'R_j'] * x_vir[(j, c)] for j in df_vir_opt.index for c in vir_classes if c in vir_risk_tasiyanlar])

    Z_anomali_udf = pulp.lpSum([df_opt.loc[i, 'A_i_Anomali_Skoru'] * x_udf[(i, c)] for i in df_opt.index for c in udf_classes if c in risk_tasiyan_kararlar])
    Z_anomali_vir = pulp.lpSum([df_vir_opt.loc[j, 'A_i_Anomali_Skoru'] * x_vir[(j, c)] for j in df_vir_opt.index for c in vir_classes if c in vir_risk_tasiyanlar])

    Z_tedarikci_etki = pulp.lpSum([y[j] for j in df_vir_opt.index])

    # Toplam Amaç Fonksiyonu (Dengelenmiş)
    prob += (w1_udf * Z_maliyet_udf + w2_udf * Z_ml_udf + w3_udf * Z_risk_udf + w4_udf * Z_anomali_udf) + \
            (w1_vir * Z_maliyet_vir + w2_vir * Z_ml_vir + w3_vir * Z_risk_vir + w4_vir * Z_anomali_vir + w5_vir * Z_tedarikci_etki), "Total_Objective"

    # 5. ÇÖZÜM VE SONUÇLARI YAZDIRMA
    prob.solve()
    durum_kodu = pulp.LpStatus[prob.status]
    print(f"Optimizasyon Çözüm Durumu: {durum_kodu}")

    # GÜVENLİK KONTROLÜ (FALLBACK MEKANİZMASI)
    if durum_kodu == 'Optimal':
        # Model başarılı çözüldü, optimizasyon kararlarını al
        df_opt['OPTIMIZE_EDILMIS_KARAR'] = [
            c for i in df_opt.index for c in udf_classes if pulp.value(x_udf[(i, c)]) == 1.0
        ]
        df_vir_opt['OPTIMIZE_EDILMIS_VIR_KARAR'] = [
            c for j in df_vir_opt.index for c in vir_classes if pulp.value(x_vir[(j, c)]) == 1.0
        ]
    else:
        # Model çözümsüz kaldı (Infeasible). Kodun çökmesini engellemek için ML'in en yüksek olasılıklı tahminiyle devam et.
        print("⚠️ DİKKAT: Kapasite kısıtları aşıldı (Infeasible). B Planı: Makine Öğrenmesi (RF) tahminleri doğrudan uygulanıyor.")

        # UDF için en yüksek olasılıklı sınıfı bul
        udf_prob_cols = [col for col in df_opt.columns if col.startswith('P_c_')]
        df_opt['OPTIMIZE_EDILMIS_KARAR'] = df_opt[udf_prob_cols].idxmax(axis=1).str.replace('P_c_', '')

        # VIR için en yüksek olasılıklı sınıfı bul
        vir_prob_cols = [col for col in df_vir_opt.columns if col.startswith('P_c_')]
        df_vir_opt['OPTIMIZE_EDILMIS_VIR_KARAR'] = df_vir_opt[vir_prob_cols].idxmax(axis=1).str.replace('P_c_', '')

    # KARAR GEREKÇELENDİRME (EXPLAINABLE AI) HESAPLAMALARI

    # 1. UDF (Üretim Hattı) Kayıtları İçin Gerekçelendirme (4 Feature)
    explain_data_udf = []
    for i in df_opt.index:
        c_star = df_opt.loc[i, 'OPTIMIZE_EDILMIS_KARAR']

        # Kararın sistemdeki ceza puanları hesaplanıyor
        v_cost = w1 * (C_c[c_star] * df_opt.loc[i, 'M_cross'])
        v_ml = w2 * (1.0 - df_opt.loc[i, f'P_c_{c_star}'])
        v_risk = w3 * df_opt.loc[i, 'R_i'] if c_star in risk_tasiyan_kararlar else 0.0
        v_anomali = w4 * df_opt.loc[i, 'A_i_Anomali_Skoru'] if c_star in risk_tasiyan_kararlar else 0.0

        total_v = v_cost + v_ml + v_risk + v_anomali
        if total_v == 0: total_v = 1e-9 # Sıfıra bölünme hatasını engellemek için

        explain_data_udf.append({
            'Etki_Maliyet_Pct': round((v_cost / total_v) * 100, 1),
            'Etki_ML_Pct': round((v_ml / total_v) * 100, 1),
            'Etki_Risk_Pct': round((v_risk / total_v) * 100, 1),
            'Etki_Anomali_Pct': round((v_anomali / total_v) * 100, 1)
        })

    # Hesaplanan yüzdeleri UDF tablosuna ekliyoruz
    df_explain_udf = pd.DataFrame(explain_data_udf, index=df_opt.index)
    df_opt = pd.concat([df_opt, df_explain_udf], axis=1)

    # 2. VIR (Tedarikçi) Kayıtları İçin Gerekçelendirme (5 Feature - pct_supplier dahil)
    explain_data_vir = []
    for j in df_vir_opt.index:
        c_star = df_vir_opt.loc[j, 'OPTIMIZE_EDILMIS_VIR_KARAR']

        v_cost = w1 * (C_c_vir[c_star] * 1.0)
        v_ml = w2 * (1.0 - df_vir_opt.loc[j, f'P_c_{c_star}'])
        v_risk = w3 * df_vir_opt.loc[j, 'R_j'] if c_star in vir_risk_tasiyanlar else 0.0
        v_anomali = w4 * df_vir_opt.loc[j, 'A_i_Anomali_Skoru'] if c_star in vir_risk_tasiyanlar else 0.0

        # pct_supplier BURADA devreye giriyor! PuLP'taki y_j değişkeninin değerini okuyoruz.
        v_supplier = w5 * pulp.value(y[j])

        total_v = v_cost + v_ml + v_risk + v_anomali + v_supplier
        if total_v == 0: total_v = 1e-9

        explain_data_vir.append({
            'Etki_Maliyet_Pct': round((v_cost / total_v) * 100, 1),
            'Etki_ML_Pct': round((v_ml / total_v) * 100, 1),
            'Etki_Risk_Pct': round((v_risk / total_v) * 100, 1),
            'Etki_Anomali_Pct': round((v_anomali / total_v) * 100, 1),
            'Etki_Tedarikci_Pct': round((v_supplier / total_v) * 100, 1) # Tedarikçi Etkisi eklendi
        })

    # Hesaplanan yüzdeleri VIR tablosuna ekliyoruz
    df_explain_vir = pd.DataFrame(explain_data_vir, index=df_vir_opt.index)
    df_vir_opt = pd.concat([df_vir_opt, df_explain_vir], axis=1)

    return df_opt, df_vir_opt, prob.status

