class DecisionShiftSimulator:
    def __init__(self, months_to_simulate=6):
        self.months_to_simulate = months_to_simulate
        self.udf_forecast_data = []
        self.vir_forecast_data = []

    def run_simulation(self):
        print(f"\n--- BAŞLIYOR: Karar ve Tedarikçi Risk Bazlı {self.months_to_simulate} Aylık Projeksiyon ---")

        # 1. Mevcut Veriyi Yükle
        df_udf_hist, df_isko_hist, df_vir_hist = load_and_preprocess_data()
        df_udf_hist, df_vir_hist = feature_engineering_and_anomaly(df_udf_hist, df_isko_hist, df_vir_hist)
        df_udf_hist, df_vir_hist, udf_classes, vir_classes = train_ml_models_and_get_probabilities(df_udf_hist, df_vir_hist)

        # TEDARİKÇİ RİSK MOMENTUMU HESAPLAMA
        # Geçmiş performansı kötü olan tedarikçilerin her ay anomali skorlarını artırarak gelecekteki çöküşlerini simüle edeceğiz.
        supplier_stats = df_vir_hist.groupby('Tedarikçi').agg(
            Toplam_Islem=('Numara', 'count'),
            Kotu_Karar_Orani=('VIR_KARAR', lambda x: x.isin(['RET', 'RU']).mean()),
            Ort_Anomali=('A_i_Anomali_Skoru', 'mean')
        )

        # Risk Momentum: Kötü karar oranı ve anomalisi yüksek olanlar her ay %X daha kötüye gider
        supplier_stats['Aylik_Bozulma_Hizi'] = (supplier_stats['Kotu_Karar_Orani'] * 0.15) + (supplier_stats['Ort_Anomali'] * 0.05)

        avg_monthly_udf = len(df_udf_hist) // 3
        avg_monthly_vir = len(df_vir_hist) // 3

        current_date = datetime.date.today()
        aktif_agirliklar = {
            'w_cost': 0.20, 'w_ml': 0.20, 'w_risk': 0.20, 'w_anomaly': 0.20, 'w_supplier': 0.20
        }

        for month_offset in range(1, self.months_to_simulate + 1):
            sim_date = current_date + relativedelta(months=month_offset)
            month_label = sim_date.strftime("%Y-%m")
            print(f"\n>> Simüle Edilen Ay: {month_label} | Çözümleniyor...")

            df_udf_sim = df_udf_hist.sample(n=avg_monthly_udf, replace=True).copy()
            df_vir_sim = df_vir_hist.sample(n=avg_monthly_vir, replace=True).copy()
            df_udf_sim.reset_index(drop=True, inplace=True)
            df_vir_sim.reset_index(drop=True, inplace=True)

            # GELECEK TEDARİKÇİ BOZULMASINI (DEGRADATION) VERİYE ENJEKTE ETME
            # İlerleyen aylarda riskli tedarikçilerin anomali skorları sentetik olarak artırılır.
            for idx, row in df_vir_sim.iterrows():
                tedarikci = row['Tedarikçi']
                if tedarikci in supplier_stats.index:
                    bozulma_hizi = supplier_stats.loc[tedarikci, 'Aylik_Bozulma_Hizi']
                    # Bileşik bozulma formülü: Mevcut_Skor * (1 + Bozulma_Hızı)^Ay
                    yeni_anomali = row['A_i_Anomali_Skoru'] * ((1 + bozulma_hizi) ** month_offset)
                    # Skoru max 1.0 ile sınırla
                    df_vir_sim.at[idx, 'A_i_Anomali_Skoru'] = min(1.0, yeni_anomali)

            # Güncellenmiş risk verileriyle optimizasyonu çalıştır
            df_opt, df_vir_opt, prob_status = run_optimization_model(df_udf_sim, df_vir_sim, udf_classes, vir_classes,
                                                                     df_isko_hist, dinamik_w=aktif_agirliklar)

            feedback_df = pd.DataFrame({
                'AI_KARAR': df_opt['OPTIMIZE_EDILMIS_KARAR'],
                'INSAN_KARAR': df_opt['KSYM_SORUMLUSU_KARAR']
            })

            aktif_agirliklar = update_dynamic_weights(feedback_df, aktif_agirliklar, alpha=0.10)

            # Sonuçları Kaydet
            df_opt['Sim_Ay_Yil'] = month_label
            df_opt['Senaryo_Insan_Karari'] = df_opt['KSYM_SORUMLUSU_KARAR']
            df_opt['Senaryo_AI_Karari'] = df_opt['OPTIMIZE_EDILMIS_KARAR']
            df_opt['SIM_UDF_NO'] = [f"F-{month_label}-U{i}" for i in range(len(df_opt))]
            udf_cols = ['Sim_Ay_Yil', 'SIM_UDF_NO', 'KALEM', 'HATA_TURU', 'Senaryo_Insan_Karari', 'Senaryo_AI_Karari', 'Etki_Maliyet_Pct', 'Etki_ML_Pct', 'Etki_Risk_Pct', 'Etki_Anomali_Pct']
            self.udf_forecast_data.append(df_opt[udf_cols])

            df_vir_opt['Sim_Ay_Yil'] = month_label
            df_vir_opt['Senaryo_Insan_Karari'] = df_vir_opt['VIR_KARAR']
            df_vir_opt['Senaryo_AI_Karari'] = df_vir_opt['OPTIMIZE_EDILMIS_VIR_KARAR']
            df_vir_opt['SIM_VIR_NO'] = [f"F-{month_label}-V{i}" for i in range(len(df_vir_opt))]
            vir_cols = ['Sim_Ay_Yil', 'SIM_VIR_NO', 'KALEM', 'Tedarikçi', 'Sapma Sınıfı', 'Senaryo_Insan_Karari', 'Senaryo_AI_Karari', 'A_i_Anomali_Skoru', 'Etki_Maliyet_Pct', 'Etki_ML_Pct', 'Etki_Risk_Pct', 'Etki_Anomali_Pct', 'Etki_Tedarikci_Pct']
            self.vir_forecast_data.append(df_vir_opt[vir_cols])

        final_udf_forecast = pd.concat(self.udf_forecast_data, ignore_index=True)
        final_vir_forecast = pd.concat(self.vir_forecast_data, ignore_index=True)

        final_udf_forecast['Karar_Degisti_Mi'] = np.where(final_udf_forecast['Senaryo_Insan_Karari'] == final_udf_forecast['Senaryo_AI_Karari'], 'Hayır', 'Evet')

        final_udf_forecast.to_csv('superset_udf_karar_simulasyonu.csv', index=False)
        final_vir_forecast.to_csv('superset_vir_karar_simulasyonu.csv', index=False)

        print("\n✅ Karar Simülasyonu Tamamlandı! Detaylı CSV dosyaları Superset için hazır.")
        return final_udf_forecast, final_vir_forecast

